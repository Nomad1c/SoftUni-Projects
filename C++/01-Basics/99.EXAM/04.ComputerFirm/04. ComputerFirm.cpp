#include <iostream>

using namespace std;

int main() {
    // ?????? ?? ???? ?? ??????????
    int n;
    cin >> n;

    double total_sales = 0;
    double total_rating = 0;

    // ?????? ?? ????? ?????????? ????? ? ??????????? ?? ?????????? ? ????????
    for (int i = 0; i < n; ++i) {
        int model;
        cin >> model;

        int sales = model / 10;  // ????????? ?? ?????????? ????????
        int rating = model % 10;  // ????????? ?? ????????

        double sales_percentage;
        switch (rating) {
        case 2:
            sales_percentage = 0;
            break;
        case 3:
            sales_percentage = 0.5;
            break;
        case 4:
            sales_percentage = 0.7;
            break;
        case 5:
            sales_percentage = 0.85;
            break;
        case 6:
            sales_percentage = 1;
            break;
        default:
            sales_percentage = 0;
        }

        double actual_sales = sales * sales_percentage;
        total_sales += actual_sales;
        total_rating += rating;
    }

    // ??????????? ?? ??????? ???????
    double average_rating = total_rating / n;

    // ????????? ?? ?????????
    cout << fixed;
    cout.precision(2);
    cout << total_sales << endl;
    cout << average_rating << endl;

    return 0;
}
