#include <iostream>

using namespace std;

int main() {
    // ????????? ?? ?????????? ?????
    int num;
    cin >> num;

    // ????????? ?? ???????
    int first = num % 10;
    num /= 10;
    int second = num % 10;
    num /= 10;
    int third = num;

    // ???????? ?? ?????????
    if (first == 0 || second == 0 || third == 0) {
        cout << "?????????? ????? ?? ? ???????!" << endl;
        return 1;
    }

    // ????? ?? ?????????
    for (int i = 1; i <= first; i++) {
        for (int j = 1; j <= second; j++) {
            for (int k = 1; k <= third; k++) {
                cout << i << " * " << j << " * " << k << " = " << i * j * k << ";" << endl;
            }
        }
    }

    return 0;
}
